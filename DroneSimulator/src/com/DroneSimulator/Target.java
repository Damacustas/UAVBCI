package com.DroneSimulator;



public class Target {

		private int height, width;
				
		public Target(){
			height = 100;
			width = 100;
		}
		
		public int getHeight(){
			return height;
		}
		
		public int getWidth(){
			return width;
		}

		public void setHeight(int height){
			this.height = height;
		}
		
		public void setWidth(int width){
			this.width = width;
		}

}
